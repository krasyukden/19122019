var a = [100, 103, 106, 4, 200, 201, 202, 203, 9, 10, 11, 12, 13, 401, 402, 403, 404, 405, 406];//4. Нужно прогрессия на мин. 3 цифры
var n = 19; // a.length
var k = 0;
var m = 0;

for(var i = 0; i < n; i++){
	k = a[i + 1] - a[i];
	if(a[i + 1] == a[i] + k && a[i + 2] == a[i] + 2 * k){
		m++;
		if(a[i] + 3 * k == a[i + 3]){
			m--;
		}
		//console.log(k);
	}
	
}
